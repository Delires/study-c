/**
小甲鱼和我一样，他末尾多个个if语句排序，让每五个数一个回车，每个数占12个字宽
**/
#include <stdio.h>
void main()
{
    int i, a[20]={1,1}; //未定义的按0赋值
    //a[20]是从a[0]开始排到a[19]
    for(i=2; i<=19; i++) //i不参与运算，只表示循环次数
    {
        a[i]=a[i-1]+a[i-2];
    }
    for(i=0; i<=19; i++)
    {
        if(0==i%5)//每5个数一个回车
        {
            printf("\n");
        }
        printf("%12d", a[i]);//每个占12个字宽
    }

}
