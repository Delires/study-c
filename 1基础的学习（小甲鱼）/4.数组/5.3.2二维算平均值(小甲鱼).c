/**
赋值这样一个表格，并求分科平均成绩和总平均成绩
             张     王     李     赵     周
    match    80     61     59     85      76
    china    75     65     63     87      77
    foxpro   92     71     70     90      85
用for循环取成绩a[j][i]

小甲鱼是先赋值列，在赋值行,所以他的表是竖着的那个表
**/
#include <stdio.h>
void main()
{
    int a[5][3]={{80,75,92},{61,65,71},{59,63,70},{85,87,90},{76,77,85}};
       //先赋值后头的
    int i, j, sum=0, v[3]; //v[3]一维数组,宽度为3
    for(i=0; i<=2; i++)
    {
        for(j=0; j<=4; j++)
        {
            sum=sum+a[j][i]; //这个表是竖过来的表
        }
        v[i]=sum/5;
        sum=0;//每循环小的一次就赋值0，不然会叠加

    }
    printf("match=%d\t china=%d\t foxpro=%d\t\n", v[1], v[2], v[3]);
    printf("average=%d", (v[1]+v[2]+v[3])/3);
}
