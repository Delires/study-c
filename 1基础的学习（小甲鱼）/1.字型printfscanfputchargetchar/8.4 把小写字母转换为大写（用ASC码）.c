#include <stdio.h>
void main()
{
    char a, b;  /* 定义ab为字符变量*/
    a = 'a';    /* a=字符‘a’ 是个数字 */
    b = 'b';    /* b=字符‘b’ 是个数字 */
    a = a-32;
    b = b-32;
    printf("%3c, %3c\n %3d, %3d\n", a, b , a, b);
    /* %C ab的字符型打印出来    %d ab整型打印出来*/
    /* 若想打印出来整齐 把%d%c 变为 %3c%3d */
}


/* 大写字母 A~Z:65~90  */
/* 小写字母 a~z:97~122 */
/* 小写ASC-32=大写ASC  */
/* 因为都是变量，所以定义时必须用 小写字母*/
