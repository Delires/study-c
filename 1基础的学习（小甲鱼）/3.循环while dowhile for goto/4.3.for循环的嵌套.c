/****
for(表达式1;表达式2;表达式3)语句
for(循环赋初值;循环的判断;循环的自增)执行的语句

循环的嵌套
外层满若足进内层，内层全部执行完，直到不满足内层再回到外层
外层+1若仍满足内层，内层再从头开始执行，直到不满足回外层
外层再+1若仍满足内层，内层再从头开始执行，直到再次不满足回外层
直到最外层也不满足，退出
(哪层满足才自增1，不满足不自增)
*****/

#include <stdio.h>
void main()
{
    int i, j, k;
    printf("i j k\n");
    for(i=0; i<100; i++)
    for(j=0; j<100; j++)
    for(k=0; k<100; k++)
    printf("%d%d%d\n", i, j, k);

}
