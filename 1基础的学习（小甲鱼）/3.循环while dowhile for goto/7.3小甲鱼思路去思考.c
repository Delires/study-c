/**
用π/4≈1-1/3+1/5-1/7+.....公式求π的近似值，直到某一项的绝对值小于0为止

小甲鱼和我的思路区别:
1.小甲鱼采用s=-s表示一正一负，我采用次方pow(-1,m)
2.小甲鱼用项的精度规定范围，我用i++规定范围
fabs()绝对值函数
**/
#include <stdio.h>
#include <math.h>
void main()
{
    int s=1;
    float i=1, pi=0;
    while(fabs(1/i) > 1e-6)// 1e-6是1*10^-6
    {                      //只要绝对值大于1*10^-6就循环
        pi=pi+s*(1.0/i);
        s=-s;         //妙，每循环一次s变号一次
        i=i+2;
    }
    printf("%f", 4*pi);

}
