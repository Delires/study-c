/**
负数转化为二进制要 按位取反再加1
如―2  2是0000 0010  取反1111 1101 再加1为 1111 1110
所以-2 二进制位 1111 11110

无符号数右移  高位补0  称逻辑右移
有符号数右移  高位补1  称算数右移
**/
#include <stdio.h>
int main(void)
{
    unsigned char a = -2; //逻辑右移
    a = a >> 1;
    printf("%d\n",a); //127 0111 1111

    signed char b = -2;   //算术右移
    b = b >> 1;
    printf("%d\n",b); //-1 1111 1111(反码，减1取反)

    return 0;
}
